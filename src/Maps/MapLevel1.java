    public GeneralElement[][] ElementMap(){
        MapLevel1 MapLevel1 = new MapLevel1();
        int[][] intMap = MapLevel1.intMap();
        GeneralElement[][] myMap = new GeneralElement[intMap.length][intMap[0].length];
        int posX = 0;
        int posY = 0;
        for (int i = 0; i < intMap.length; i++) {
            posY = i * 25;
            for (int j = 0; j < intMap[i].length; j++) {
                posX = j * 25;
                switch (intMap[i][j]){
                    case 0:
                        myMap[i][j] = new Empty(posX, posY);
                        break;
                    case 1:
                        myMap[i][j] = new Block(posX, posY);
                        break;
                    case 2:
                        myMap[i][j] = new Coin(posX, posY);
                        break;
                    case 3:
                        myMap[i][j] = new BigCoin(posX, posY);
                        break;
                    case 5:
                        myMap[i][j] = new Channel(posX, posY);
                }
            }
        }
        return myMap;
    }








